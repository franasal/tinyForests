class PlantData {
  final String scientificName;
  final String commonName;
  final String commonNameEn;
  final String commonNameDe;
  final String commonNameEs;
  String pathPicture;
  final String plantType;
  String distribution;
  final String maxSizeMeters;
  final int totalPlanted;
  int localPlanted;

  final String lifeExpectancyYears;
  final String frequency;
  final String habitat;
  final String leafType;
  final String floweringSeason;
  final String conservationStatus;
  final String interestingFact;

  PlantData(
      {required this.scientificName,
      required this.commonName,
      required this.commonNameEn,
      required this.commonNameDe,
      required this.commonNameEs,
      required this.distribution,
      required this.maxSizeMeters,
      required this.totalPlanted,
      required this.localPlanted,
      required this.lifeExpectancyYears,
      required this.frequency,
      required this.habitat,
      required this.leafType,
      required this.floweringSeason,
      required this.conservationStatus,
      required this.interestingFact,
      required this.pathPicture,
      required this.plantType});

  @override
  String toString() {
    return 'PlantData(\n'
        '  scientificName: "$scientificName",\n'
        '  commonName: "$commonName",\n'
        '  commonNameEn: "$commonNameEn",\n'
        '  commonNameDe: "$commonNameDe",\n'
        '  commonNameEs: "$commonNameEs",\n'
        '  distribution: "$distribution",\n'
        '  maxSizeMeters: "$maxSizeMeters",\n'
        '  totalPlanted: $totalPlanted,\n'
        '  localPlanted: $localPlanted,\n'
        '  lifeExpectancyYears: "$lifeExpectancyYears",\n'
        '  frequency: "$frequency",\n'
        '  habitat: "$habitat",\n'
        '  leafType: "$leafType",\n'
        '  floweringSeason: "$floweringSeason",\n'
        '  conservationStatus: "$conservationStatus",\n'
        '  interestingFact: "$interestingFact",\n'
        '  pathPicture: "$pathPicture",\n'
        '  plantType: "$plantType"\n'
        ')';
  }
}

